#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
	DayViewController *dayViewController = [[DayViewController alloc] initWithNibName:@"DayViewController" bundle:nil];
	dayViewController.yearIndex = self.index;
	dayViewController.monthIndex = indexPath.row;
	dayViewController.managedObjectContext = self.managedObjectContext;
	dayViewController.nestedArray = self.nestedArray;
	[self.navigationController pushViewController:dayViewController animated:YES];
	[dayViewController release];
}

- (void)updateTable:(NSArray *)array {
	self.nestedArray = array;
	[self.tableView reloadData];
	[super updateTable:array];
}

